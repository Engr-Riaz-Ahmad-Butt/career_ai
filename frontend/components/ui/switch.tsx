"use client"

import * as React from "react"
import { cn } from "@/lib/utils"

const Switch = React.forwardRef<
    HTMLInputElement,
    React.InputHTMLAttributes<HTMLInputElement> & { onCheckedChange?: (checked: boolean) => void }
>(({ className, onCheckedChange, ...props }, ref) => (
    <div className="relative inline-flex items-center">
        <input
            type="checkbox"
            role="switch"
            className={cn(
                "peer h-6 w-11 shrink-0 cursor-pointer appearance-none rounded-full border-2 border-transparent transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-ring focus-visible:ring-offset-2 focus-visible:ring-offset-background disabled:cursor-not-allowed disabled:opacity-50 checked:bg-primary bg-input",
                className
            )}
            ref={ref}
            onChange={(e) => onCheckedChange?.(e.target.checked)}
            {...props}
        />
    </div>
))
Switch.displayName = "Switch"

export { Switch }
